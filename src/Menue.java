    private void jTable1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseReleased
        // TODO add your handling code here:
        int i=jTable1.getSelectedRow();
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        txtcentre.setSelectedItem(model.getValueAt(i,0).toString());
        txtrec.setText(model.getValueAt(i, 1).toString());
        txtobj.setText(model.getValueAt(i, 2).toString());
        txttaux.setText(model.getValueAt(i, 3).toString());
        try {
            java.util.Date date= new SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(i, 4));
            txtdate.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(Menue.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jTable1MouseReleased

