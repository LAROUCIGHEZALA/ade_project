    public void Table(){
        String []recouvrement={"centre","rec","obj","taux","date"};
        String [] afficher=new String[5];
        DefaultTableModel model=new DefaultTableModel(null,recouvrement);
        try{
               Class.forName("com.mysql.jdbc.Driver");
      java.sql.Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3307/Stageade","root","");   
            
         java.sql.Statement stmt=con.createStatement();
          ResultSet rs =stmt.executeQuery("SELECT * FROM recouvrement");
          while(rs.next()){
                    afficher[0]=rs.getString("centre");
                    afficher[1]=rs.getString("rec");
                    afficher[2]=rs.getString("obj");
                    afficher[3]=rs.getString("taux");
                    afficher[4]=rs.getString("date");
                    model.addRow(afficher);
                    
          
          
          }
          jTable1.setModel(model);
          con.close();
        }
        catch(Exception e){
            e.printStackTrace();
        
        
        }
       
        
        
    
    }
